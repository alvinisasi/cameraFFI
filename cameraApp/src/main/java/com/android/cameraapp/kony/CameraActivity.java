    public static void show(Function listenerResponse){
        listener = listenerResponse;
        KonyMain act = KonyMain.getActContext();
        Intent intent = new Intent(KonyMain.getAppContext(), CameraActivity.class);
        act.startActivity(intent);
        LocalBroadcastManager.getInstance((Context)KonyMain.getAppContext()).registerReceiver(broadcastReceiver, new IntentFilter("qrScanner"));
    }

    private static BroadcastReceiver broadcastReceiver = new BroadcastReceiver() {
        @Override
        public void onReceive(Context context, Intent intent) {
            if (intent.hasExtra("qrResult")) {
                String qrResult = intent.getStringExtra("qrResult");
                Object[] objectKony = new Object[]{qrResult};
                try {
                    listener.execute(objectKony);
                    LocalBroadcastManager.getInstance(KonyMain.getAppContext()).unregisterReceiver(broadcastReceiver);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else if (intent.hasExtra("qrInput")) {
                Object[] objectKony = new Object[]{"qrInput"};
                try {
                    listener.execute(objectKony);
                    LocalBroadcastManager.getInstance(KonyMain.getAppContext()).unregisterReceiver(broadcastReceiver);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else if (intent.hasExtra("qrFailed")) {
                Object[] objectKony = new Object[]{"qrFailed"};
                try {
                    listener.execute(objectKony);
                    LocalBroadcastManager.getInstance(KonyMain.getAppContext()).unregisterReceiver(broadcastReceiver);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else if (intent.hasExtra("qrBack")) {
                Object[] objectKony = new Object[]{"qrBack"};
                try {
                    listener.execute(objectKony);
                    LocalBroadcastManager.getInstance(KonyMain.getAppContext()).unregisterReceiver(broadcastReceiver);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        }
    };

